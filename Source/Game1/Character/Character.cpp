#include "../../../pch.h"
#include "Character.h"
#include "../../GameBase/Sprite/Sprite.h"

using namespace Math;

/////////////////////////////////////////////////////
// Name : Character
//
// Over View : コンストラクタ
//
// Argument : 無し
/////////////////////////////////////////////////////
Character::Character(wchar_t* texturePass)
	:vel_(Vector2::Zero), moveSpeed_(1.0f)
{
	sprite_.reset(new Sprite(texturePass));
}

/////////////////////////////////////////////////////
// Name : ~Character
//
// Over View : デストラクタ
//
// Argument : 無し
/////////////////////////////////////////////////////
Character::~Character()
{
}

/////////////////////////////////////////////////////
// Name : Initialize
//
// Over View : 初期化処理
//
// Argument : 無し
//
// Return : 無し
/////////////////////////////////////////////////////
void Character::Initialize()
{
}

/////////////////////////////////////////////////////
// Name : Update
//
// Over View : 更新処理
//
// Argument : 無し
//
// Return : 無し
/////////////////////////////////////////////////////
void Character::Update()
{
	sprite_->Pos(sprite_->Pos() + vel_ * moveSpeed_);
}

/////////////////////////////////////////////////////
// Name : Render
//
// Over View : 描画処理
//
// Argument : 無し
//
// Return : 無し
/////////////////////////////////////////////////////
void Character::Render()
{
	sprite_->Render();
}
